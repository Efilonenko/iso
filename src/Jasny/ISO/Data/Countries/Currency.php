    /**
     * Get country code
     *
     * @param string $country
     * @return string
     */
    protected static function countryCode($country)
    {
        $country = (string)$country;
        if (strlen($country) != 2) $country = En::getCode($country);

        return $country;
    }

    /**
     * Get currency param
     *
     * @param string $country
     * @param string $param
     * @return string
     */
    protected static function getCurrencyParam($country, $param)
    {
        if (isset(static::$list[static::countryCode($country)])) {
            $result = [];
            for ($i = 0; $i < count(static::$list[static::countryCode($country)]); $i++) {
                array_push($result, static::$list[static::countryCode($country)][$i][$param]);
            }
            return implode(", ", array_unique($result));
        } else {
            return null;
        }
    }

    /**
     * Get list of currencies
     *
     * @return array
     */
    public static function getList()
    {
        return static::$list;
    }

    /**
     * Get full info about country currency
     *
     * @param string $country
     * @return string
     */
    public static function getFullInfo($country)
    {
        if (isset(static::$list[static::countryCode($country)])) {
            $result = [];
            for ($i = 0; $i < count(static::$list[static::countryCode($country)]); $i++) {
                $fullInfo =
                    static::$list[static::countryCode($country)][$i]["name"]
                    . " - " . static::$list[static::countryCode($country)][$i]["code"] . "("
                    . static::$list[static::countryCode($country)][$i]["sign"] . "), "
                    . static::$list[static::countryCode($country)][$i]["fractal unit"] . "("
                    . static::$list[static::countryCode($country)][$i]["fractal division"] . ")";
                array_push($result, $fullInfo);
            }
            return implode("; ", array_unique($result));
        } else {
            return null;
        }
    }

    /**
     * Get country currency name
     *
     * @param string $country
     * @return string
